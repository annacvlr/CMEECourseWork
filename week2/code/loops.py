#!/usr/bin/env python3

""" Demonstrates how to use 
    - FOR loops: for iterating over a sequence
    - WHILE loops: continues WHILE a specific condition is TRUE
    """


#FOR loops 
for i in range(5):
    print(i)

my_list = [0,2, "geronimo!", 3.0, True, False]
for k in my_list:
    print(k)



#WHILE loops
z=0
while z < 100:
    z=z+1
    print(z)
